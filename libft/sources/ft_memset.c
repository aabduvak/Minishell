/* ************************************************************************** */
/*	void * -> memory block													  */
/*	(char *) -> pointer														  */
/*	*(char *) -> character (data)											  */
/* ************************************************************************** */

#include "libft.h"

void	*ft_memset(void *ptr, int c, size_t size)
{
	size_t	i;

	i = 0;
	while (size > i)
	{
		((unsigned char *)ptr)[i] = (unsigned char) c;
		i++;
	}
	return (ptr);
}
